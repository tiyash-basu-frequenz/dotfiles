-- set tab to 4 spaces
vim.cmd("set expandtab")
vim.cmd("set tabstop=4")
vim.cmd("set softtabstop=4")
vim.cmd("set shiftwidth=4")

-- spellcheck
vim.cmd("set spell")
vim.cmd("set spelllang=en_us")

-- enable line numbers
vim.cmd("set number")

-- copy to clipboard
vim.cmd("set clipboard+=unnamedplus")

-- set rulers
vim.cmd("set colorcolumn=80,100,120")
